      <p class="text-justify">Se você é um verdadeiro apaixonado por café, sabe bem que a escolha do grão faz toda a diferença na hora de poder degustar com maior satisfação.
        Quando você toma o seu café, está consumindo grãos que podem ser de duas espécies conhecidas por: Arábica (Coffea arabica) e Robusta (Coffea canephora). Assim como os nomes são diferentes, também sabemos que ambas têm outras tantas características, responsáveis pelos aromas e sabores do café! Quer entender mais sobre o assunto? Então, vamos lá!</p>
        <p class="text-justify font-weight-bold">Café Arábica</p>
        <p class="text-justify">A planta que dá origem a esta variedade do grão é proveniente das montanhas da Etiópia. Sabe-se que a altitude está amplamente relacionada à qualidade do grão produzido; quanto mais alta for a lavoura, maior será a qualidade do grão.
          É por esse motivo que os grãos que são cultivados acima de 1.000 metros de altitude, costumam ter um sabor impecável. O café arábica possui 50% menos cafeína, mas possui um teor maior de açúcares, responsáveis pela complexidade de sabores dessa variedade. Os cafés finos produzidos no mundo são das variedades dessa espécie (mundo novo, catuaí amarelo e vermelho, bourbon etc.).</p>
        <br>
        <p class="text-justify font-weight-bold">Café Bourbon</p>
        <p class="text-justify">Essa é uma das variedades mais conhecidas no mundo! Ela deriva do Arábica e é bastante popular, principalmente, fora do Brasil. Entre as características peculiares que ela possui, estão a presença de notas mais achocolatadas, o aroma bem forte e marcante, e a acidez moderada.
          É indicada para pessoas que gostam de explorar um sabor mais leve e adocicado, já que possui notas que lembram o caramelo.</p>
        <br>
        <p class="text-justify font-weight-bold">Café Kona</p>
        <p class="text-justify">Ele recebe este nome porque é cultivado na cidade de Kona, Havaí. O clima local e o solo vulcânico da região são propícios para o cultivo dessa variedade exótica de arábica.
          Se você é fã de um bom café gourmet, então o café Kona será uma experiência inesquecível! Para algumas pessoas, este é o melhor café do mundo!</p>
        <br>
        <p class="text-justify font-weight-bold">Café Catuaí</p>
        <p class="text-justify">Para que você consiga extrair todo o potencial dessa variedade, é indispensável que seja cultivado em altitudes acima de 900 m! Ele é muito cultivado em nosso país.
          Possui acidez moderada e dispensa qualquer adição de açúcar ou adoçante (a seu gosto, claro!), pois possui doçura natural.</p>
        <br>
        <p class="text-justify font-weight-bold">Café Acaiá</p>
        <p class="text-justify">A variedade Acaiá originou-se de plantas da cultivar Mundo Novo, uma das mais cultivadas aqui no Brasil. Acaiá significa “frutos com sementes grandes” em tupi-guarani, o que caracteriza essa variedade.
          Esse tipo de grão é indicado para pessoas que preferem um sabor mais suave, pois costuma apresentar notas que lhe remetem a frutas. Ele tem um sabor achocolatado e acidez média.</p>
        <br>
        <p class="text-justify font-weight-bold">Café Robusta</p>
        <p class="text-justify">Também conhecido por Conilon, o café selecionado com este tipo de grão tem uma concentração de cafeína bem maior, ou seja, ele é perfeito para pessoas que gostam de um sabor amargo.
          A planta Coffea canephora é bastante cultivada em diversos países, principalmente no Vietnã e no Brasil.
          Ela se desenvolve com bastante facilidade em regiões com clima quente e úmido, e em altitudes mais baixas, até 600 metros de altitude. Ela também apresenta mais resistência aos ataques de pragas em relação ao café arábica, por isso mesmo torna-se prático e de fácil plantio.
          Um de seus usos mais comuns está, justamente, no café instantâneo. Quando adiciona-se este grão ao preparo, percebemos que o café fica mais encorpado e com um sabor mais amargo também.</p>
